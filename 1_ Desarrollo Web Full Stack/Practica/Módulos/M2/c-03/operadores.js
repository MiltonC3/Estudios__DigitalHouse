// ----------------- OPERADORES -----------------

// *Suma
console.log(5 + 10);

// *Resta
console.log(10 - 6);

// *Multiplicación
console.log(3 * 4);

// *División
console.log(10 / 2);

// *Módulo
console.log(10 % 2);

// *Incremento
let numero = 6;
console.log(++numero);

// *Decremento
let otroNumero = 10;
console.log(--otroNumero);

console.log("=======================================");
// ---------------- COMPARACIONES ----------------

// *Comparación simple
console.log(5 == "5");

// *Comparación estricta
console.log(5 === "5");

// *Desigualdad simple
console.log(5 != "5");

// *Desigualdad estricta
console.log(5 !== "5");

// *Mayor que
console.log(6 > 6);

// *Mayor o igual que
console.log(6 >= 6);

// *Menor que
console.log(7 < 7);

// *Menor o igual que
console.log(6 <= 6);

console.log("=======================================");
// --------------- OPERADORES LÓGICOS ----------------

// * AND &&
console.log(false && true);

// * OR ||
console.log(false || true);

