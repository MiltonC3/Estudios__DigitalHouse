// ---------------- VARIABLES ---------------------

// *var - puede ser redifinido, cambiar su valor y el tipo de dato
var nombre = "Nacho";
console.log(nombre);

// *var - no puede ser redifinido, pero si cambiar su valor pero no el tipo de dato
let numero_preferido = 3;
console.log(numero_preferido);

// *const - no puede ser redifinido, ni cambiar el valor y tampoco el tipo de dato
const apellido = "Vuotto";
console.log(apellido);
